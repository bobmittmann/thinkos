int pa75_get_status_cgi(struct httpctl * ctl)
{
	char s[HTML_MAX];
	char * cp = s;
	uint32_t supv;
	int n;

	supv = line_supv_get();

   	http_200(ctl, APPLICATION_JSON);
	cp += snprintf(cp, HTML_MAX, "{\"line\":{\"offs\":%d,", line_offset_get());
	cp += snprintf(cp, HTML_MAX, " \"supv\":%d.%03d", 
				   supv / 1000, supv % 1000);
	cp += snprintf(cp, HTML_MAX, "}}\r\n");
	n = (cp - s);

	return http_send(ctl, s, n);
}

static const char * const json_err_msg[] = {
		"{\"ret\": \"OK\"}\r\n",
		"{\"error\":{\"code\":1,\"msg\":\"failed!\"}}\r\n",
		"{\"error\":{\"code\":2,\"msg\":\"failed!\"}}\r\n",
};

int reply_json_err(struct httpctl * http, int code)
{
	const char * msg;

	http_200(http, APPLICATION_JSON);
	msg = json_err_msg[code];
	return http_send(http, msg, strlen(msg));
}

int reply_json_ok(struct httpctl * http)
{
	const char * msg;

	http_200(http, APPLICATION_JSON);
	msg = json_err_msg[0];
	return http_send(http, msg, strlen(msg));
}

int pa75_set_dc_offset_cgi(struct httpctl * http)
{
	char * val;
    int32_t offset;

   	if ((val = http_query_lookup(http, "offset")) == NULL) {
		WARN("'offset' key not found!");
		return reply_json_err(http, 1);
	}

	INF("VAL='%s'", val);
   	offset = atoi(val);
	line_offset_set(offset);
	INF("Speaker line DC offset=%d!", offset);

	return reply_json_ok(http);
}


int rtsp_demo_cgi(struct httpctl * ctl)
{
	send_play_form(ctl);
	return http_send(ctl, footer_html, sizeof(footer_html) - 1);
}


int play_cgi(struct httpctl * ctl)
{
	strcpy(rtsp_host, http_query_lookup(ctl, "host"));
	strcpy(rtsp_media, http_query_lookup(ctl, "mrl"));
	rtsp_port = atoi(http_query_lookup(ctl, "port"));
	audio_level = atoi(http_query_lookup(ctl, "vol"));
	if (audio_level > 800)
		audio_level = 800;

	if (rtsp_connect(&rtsp, rtsp_host, rtsp_port, rtsp_media) < 0) {
		int code;

		WARN("RTSP connection failed!");
		send_play_form(ctl);
		send_error(ctl, "RTSP playback failed");
		if ((code = rtsp_response_code(&rtsp)) > 200) {
			http_send(ctl, rtsp_iframe_html, sizeof(rtsp_iframe_html) - 1);
		}
	} else {
		audio_gain_set(audio_level * Q15(1.0) / 100);
		send_stop_form(ctl);
	}

	return http_send(ctl, footer_html, sizeof(footer_html) - 1);
}

int stop_cgi(struct httpctl * ctl)
{
	if (rtsp_teardown(&rtsp) < 0) {
		WARN("RTSP teardown failed!");
	}
	send_play_form(ctl);
	return http_send(ctl, footer_html, sizeof(footer_html) - 1);
}

