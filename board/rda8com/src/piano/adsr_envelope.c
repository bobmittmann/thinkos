int synth_env_set(struct addsynth *synth, uint32_t k1, uint32_t k2)
{
	float t = synth->t;
	float c1;
	float c2;
	float e1 = (float)1.0;
	float e2 = (float)1.0;
	float x1 = (float)0.0;
	float x0 = (float)0.0;

	c1 = (float)1.0 - k1 * t;
	c2 = (float)1.0 - k2 * t;

	x1 = e1 - e2;
	/* find max */
	do {
		x0 = x1;
		x1 = e1 - e2;
		e1 *= c1;
		e2 *= c2;
	} while (x1 >= x0);

	//printf("x0=%f\n", (double)x0);

	synth->env.e0 = (float)1.0 / x0;
	synth->env.c1 = c1;
	synth->env.c2 = c2;

	return 0;
}


/* Conversion form float to fixed point Q1.31 */
static inline int32_t f2q31(float x) {
	return (int32_t)((float)x * (float)(2147483648.));
}

int synth_set(struct addsynth *synth, float freq, 
				float ampl, uint32_t k1, uint32_t k2)
{
	float t = synth->t;
	float w;

	/* Normalized frequency */
	w = 2 * freq * t;

	synth->osc.dp = f2q31(w);

	synth->env.c1 = (float)1.0 - k1 * t;
	synth->env.c2 = (float)1.0 - (k1 + k2) * t;

	synth->a = ampl;

	synth->osc.p = 0;
	synth->env.e1 = 0;
	synth->env.e2 = 0;

	return 0;
}

int synth_init(struct addsynth *synth, float samplerate, float ampl)
{
	synth->t = (float)1.0 / samplerate;
	synth->a = ampl;

	synth->env.c1 = 1;
	synth->env.c2 = 0;
	synth->env.e1 = ampl;
	synth->env.e2 = 0;

	synth->osc.p = 0;
	synth->osc.dp = 0;

	return 0;
}

