#define DX ((int32_t)(1 << (32 - LOG2_N)))
#define DX_MSK ((uint32_t)(DX - 1))
#define DX_RCP ((float)1.0 / DX)
#define Q31_RAD ((float)0x1.921fb6p-30)

static inline float __fsin(uint32_t x)
{
	float sin_x0;
	float cos_x0;
	float sin_fx;
	float cos_fx;
	int32_t dx0;
	float rdx0;
	float y;
	uint32_t i;

	i = (x >> (32 - LOG2_N));

	sin_x0 = qtab[i][0];
	cos_x0 = qtab[i][1];

	dx0 = x & DX_MSK;
	rdx0 = dx0 * Q31_RAD;

	sin_fx = rdx0;
	cos_fx = 1 - (rdx0 * rdx0) / 2;

	y = sin_x0 * cos_fx + cos_x0 * sin_fx;

	return y;
}

/* float pcm encoding */
int synth_pcm_encode(struct addsynth *syth, float pcm[], unsigned int len)
{
	unsigned int i;
	unsigned int j;
	uint32_t p[SYNTH_N];
	uint32_t dp[SYNTH_N];
	float a[SYNTH_N];
	float e1;
	float e2;
	float c1;
	float c2;

	for (j = 0; j < len; ++i) {
		p[j] = synth->osc.p[j];
		dp[j] = synth->osc.dp[j];
		a[j] = synth->osc.a[j];
	}

	e1 = synth->env.e1;
	e2 = synth->env.e2;
	c1 = synth->env.c1;
	c2 = synth->env.c2;

	for (i = 0; i < len; ++i) {
		float y;

		y = 0;
		for (j = 0; j < len; ++i) {
			y += __fsin(p[j]) * a[j];
			p[j] += dp[j];
		}
		
		/* Apply the envelope */
		y *= (e1 - e2);
		pcm[i] += y;

		/* Compute the envelope */
		e1 *= c1;
		e2 *= c2;
	}

	for (j = 0; j < len; ++i) {
		synth->osc.p[j] = p[j];
	}

	synth->env.e1 = e1;
	synth->env.e2 = e2;

	return len;
}

