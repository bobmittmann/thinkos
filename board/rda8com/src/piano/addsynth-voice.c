int addsynth_voice_reset(struct addsynth_voice *voice, uint32_t clk)
{
	unsigned int j;

	for (j = 0; j < ADDSYNTH_OSC_MAX; ++j) {
		voice->osc[j].p = 0;
	}

	return 0;
}

/* Conversion form float to fixed point Q1.31 */
static inline int32_t f2q31(float x) {
	return (int32_t)((float)x * (float)(2147483648.));
}

int addsynth_voice_osc_set(struct addsynth_voice * voice, float dt,
						   const struct osc_cfg cfg[], unsigned int cnt)
{
	unsigned int j;

	if (cnt > ADDSYNTH_OSC_MAX) 
		cnt = ADDSYNTH_OSC_MAX;

	for (j = 0; j < cnt; ++j) {
		float w;

		/* Normalized frequency */
		w = 2 * cfg[j].freq * dt;

		voice->osc[j].dp = f2q31(w);
		voice->osc[j].a = cfg[j].ampl;
	}

	for (; j < ADDSYNTH_OSC_MAX; ++j) {
		voice->osc[j].dp = 0;
		voice->osc[j].a = 0;
	}

	return cnt;
}

