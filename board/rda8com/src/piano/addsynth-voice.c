#define DX ((int32_t)(1 << (32 - LOG2_N)))
#define DX_MSK ((uint32_t)(DX - 1))
#define DX_RCP ((float)1.0 / DX)

static inline float __fsin(uint32_t x)
{
	float sin_x0;
	float sin_x1;
	int32_t dx0;
	float qx0;
	float y;
	uint32_t i;

	i = (x >> (32 - LOG2_N));

	dx0 = x & DX_MSK;
	qx0 = dx0 * DX_RCP;

	sin_x0 = __sintab[i];
	sin_x1 = __sintab[i + 1];

	y = sin_x0 + (sin_x1 - sin_x0) * qx0;

	return y;
}

/* float PCM encoding */
int addsynth_voice_pcm_encode(struct addsynth_voice  * voice, 
							  float pcm[], unsigned int len, uint32_t clk)
{
	uint32_t p[ADDSYNTH_OSC_MAX];
	uint32_t dp[ADDSYNTH_OSC_MAX];
	float a[ADDSYNTH_OSC_MAX];
	unsigned int i;
	unsigned int j;

//	printf(".");

	/* Oscillators */
	for (j = 0; j < ADDSYNTH_OSC_MAX; ++j) {
		p[j] = voice->osc[j].p;
		dp[j] = voice->osc[j].dp;
		a[j] = voice->osc[j].a;
	}

	for (i = 0; i < len; ++i) {
		float y;

		y = 0;
		for (j = 0; j < ADDSYNTH_OSC_MAX; ++j) {
			y += __fsin(p[j]) * a[j];
			p[j] += dp[j];
		}

		pcm[i] = y;
	}

	for (j = 0; j < ADDSYNTH_OSC_MAX; ++j) {
		voice->osc[j].p = p[j];
	}

	return len;
}

/* float PCM encoding */
int __addsynth_voice_pcm_encode(struct addsynth_voice  * voice, 
							  float pcm[], unsigned int len, uint32_t clk)
{
	uint32_t p;
	uint32_t dp;
	float a;
	unsigned int i;
	unsigned int j;

	/* Oscillators */
	for (j = 0; j < ADDSYNTH_OSC_MAX; ++j) {
		p = voice->osc[j].p;
		dp = voice->osc[j].dp;
		a = voice->osc[j].a;

		for (i = 0; i < len; ++i) {
			float y;

			y = __fsin(p) * a;
			pcm[i] += y;

			p += dp;
		}

		voice->osc[j].p = p;
	}

	return len;
}

